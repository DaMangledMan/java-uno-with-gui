    private void drawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drawButtonActionPerformed
        try {
            game.submitDrawCard(game.getCurrentPlayerName());
        }
        catch(InvalidPlayerTurnException ex) {
            Logger.getLogger(GameStage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JLabel message = new JLabel(game.getPreviousPlayerName() + " drew a card!");
        message.setFont(new Font("Arial", Font.BOLD, 48));
        JOptionPane.showMessageDialog(null, message);

        this.setPidName(game.getCurrentPlayerName());
        this.setButtonIcons();
    }//GEN-LAST:event_drawButtonActionPerformed

