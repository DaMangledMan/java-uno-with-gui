    public GameStage(ArrayList<String> playerIds) {
        initComponents();
        temp = playerIds;
        pids = temp.toArray(new String[temp.size()]);
        game = new Game(pids);
        populateArrayList();
        UnoCard firstDiscard = game.start();
        topDiscard.setText(game.getCardName(firstDiscard));
        this.lastDiscard = firstDiscard;
        setPidName();
        setButtonIcons();
    }
    
    public void setButtonIcons() {
        
        for (int i=0; i < game.getPlayerHandSize(game.getCurrentPlayerName()); i++) {
            cardButtons.get(i).setText(game.getCardName(game.getPlayerCard(game.getCurrentPlayerName(), i)));
        }
        for (int i = cardButtons.size(); i < cardButtons.size(); i++) {
            cardButtons.get(i).setIcon(null);
        }
        this.lastDiscard = this.game.getLastDiscard();
        topDiscard.setText(this.lastDiscard.toString());
    }
    
